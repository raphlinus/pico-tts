use std::borrow::Cow;

pub struct Phoneme {
    pub kind: Kind,
    pub voiced: bool,
    pub ks: Cow<'static, [f64]>,
    pub rms: f64,
}

#[derive(Clone, Copy, PartialEq)]
pub enum Kind {
    Vowel,
    Fricative,
    Nasal,
    Plosive,
    Affricate,
}

const UNVOICED_MUL: f64 = 0.1;

impl Phoneme {
    const fn vowel(ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Vowel,
            voiced: true,
            ks: Cow::Borrowed(ks),
            rms,
        }
    }

    const fn fricative(ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Fricative,
            voiced: false,
            ks: Cow::Borrowed(ks),
            rms: rms * UNVOICED_MUL,
        }
    }

    const fn fricative_v(ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Fricative,
            voiced: true,
            ks: Cow::Borrowed(ks),
            rms: rms * UNVOICED_MUL,
        }
    }

    const fn nasal(ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Nasal,
            voiced: true,
            ks: Cow::Borrowed(ks),
            rms,
        }
    }

    const fn plosive(voiced: bool, ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Plosive,
            voiced,
            ks: Cow::Borrowed(ks),
            rms,
        }
    }

    const fn affricate(voiced: bool, ks: &'static [f64], rms: f64) -> Self {
        Self {
            kind: Kind::Affricate,
            voiced,
            ks: Cow::Borrowed(ks),
            rms,
        }
    }
}

